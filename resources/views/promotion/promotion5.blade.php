    // Flip Countdown Timer Script
document.addEventListener('DOMContentLoaded', function() {
  // Set the date we're counting down to (3 days from now)
  const now = new Date();
  const countDownDate = new Date(now);
  countDownDate.setDate(now.getDate() + 3);
  countDownDate.setHours(23, 59, 59, 0);

  // Previous values for flip animation
  let prevDays, prevHours, prevMinutes, prevSeconds;

  // Update the countdown every 1 second
  const countdownTimer = setInterval(function() {
    // Get current date and time
    const currentTime = new Date().getTime();

    // Calculate the remaining time
    const timeRemaining = countDownDate - currentTime;

    // Calculate days, hours, minutes and seconds
    const days = Math.floor(timeRemaining / (1000 * 60 * 60 * 24));
    const hours = Math.floor((timeRemaining % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((timeRemaining % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((timeRemaining % (1000 * 60)) / 1000);

    // Format with leading zeros
    const formattedDays = days < 10 ? "0" + days : days;
    const formattedHours = hours < 10 ? "0" + hours : hours;
    const formattedMinutes = minutes < 10 ? "0" + minutes : minutes;
    const formattedSeconds = seconds < 10 ? "0" + seconds : seconds;

    // Apply flip animation if values changed
    if (formattedDays !== prevDays) {
      flipAnimation("days", formattedDays);
      prevDays = formattedDays;
    }
    if (formattedHours !== prevHours) {
      flipAnimation("hours", formattedHours);
      prevHours = formattedHours;
    }
    if (formattedMinutes !== prevMinutes) {
      flipAnimation("minutes", formattedMinutes);
      prevMinutes = formattedMinutes;
    }
    if (formattedSeconds !== prevSeconds) {
      flipAnimation("seconds", formattedSeconds);
      prevSeconds = formattedSeconds;
    }

    // If the countdown is finished, display a message
    if (timeRemaining < 0) {
      clearInterval(countdownTimer);
      document.getElementById("days").innerHTML = "00";
      document.getElementById("hours").innerHTML = "00";
      document.getElementById("minutes").innerHTML = "00";
      document.getElementById("seconds").innerHTML = "00";
      
      // Show expired message
      const countdownElement = document.getElementById("countdown");
      const expiredMessage = document.createElement("div");
      expiredMessage.className = "absolute inset-0 flex items-center justify-center bg-black bg-opacity-70 rounded";
      expiredMessage.innerHTML = "<span class='text-white font-bold text-lg'>โปรโมชันหมดเวลาแล้ว!</span>";
      countdownElement.parentNode.style.position = "relative";
      countdownElement.parentNode.appendChild(expiredMessage);
    }
  }, 1000);

  // Function to create flip animation
  function flipAnimation(elementId, newValue) {
    const element = document.getElementById(elementId);
    
    // Create the flip effect
    element.style.transform = "rotateX(90deg)";
    element.style.transition = "transform 0.3s";
    
    setTimeout(function() {
      element.innerHTML = newValue;
      element.style.transform = "rotateX(0deg)";
    }, 300);
  }
});

