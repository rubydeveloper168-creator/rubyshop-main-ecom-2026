document.addEventListener('DOMContentLoaded', function() {
  // Get all FAQ toggle buttons
  const faqToggles = document.querySelectorAll('.faq-toggle');
  
  // Add click event listener to each toggle button
  faqToggles.forEach((toggle, index) => {
    // Add proper ARIA attributes for accessibility
    const contentId = 'faq-content-' + index;
    const content = toggle.nextElementSibling;
    
    // Set ARIA attributes
    toggle.setAttribute('aria-expanded', 'false');
    toggle.setAttribute('aria-controls', contentId);
    toggle.setAttribute('role', 'button');
    toggle.setAttribute('tabindex', '0');
    content.setAttribute('id', contentId);
    content.setAttribute('aria-hidden', 'true');
    content.setAttribute('role', 'region');
    
    toggle.addEventListener('click', function() {
      toggleFaq(this);
    });
  });
  
  // Function to toggle FAQ item
  function toggleFaq(toggle) {
    // Toggle active class on the button
    toggle.classList.toggle('active');
    
    // Update ARIA attributes
    const isExpanded = toggle.classList.contains('active');
    toggle.setAttribute('aria-expanded', isExpanded ? 'true' : 'false');
    
    // Get the content panel
    const content = toggle.nextElementSibling;
    content.setAttribute('aria-hidden', isExpanded ? 'false' : 'true');
    
    // Toggle the active class on the content
    content.classList.toggle('active');
    
    // Toggle the hidden class
    if (content.classList.contains('hidden')) {
      content.classList.remove('hidden');
      
      // Allow the browser to calculate the height
      requestAnimationFrame(() => {
        content.style.maxHeight = content.scrollHeight + 'px';
        content.style.opacity = '1';
      });
    } else {
      content.style.maxHeight = '0';
      content.style.opacity = '0';
      
      // Add hidden class after transition completes
      setTimeout(() => {
        if (!content.classList.contains('active')) {
          content.classList.add('hidden');
        }
      }, 300); // Match transition duration
    }
    
    // Close other open FAQs (accordion behavior)
    faqToggles.forEach(otherToggle => {
      if (otherToggle !== toggle && otherToggle.classList.contains('active')) {
        // Remove active class from other toggle
        otherToggle.classList.remove('active');
        otherToggle.setAttribute('aria-expanded', 'false');
        
        // Get the other content panel
        const otherContent = otherToggle.nextElementSibling;
        otherContent.setAttribute('aria-hidden', 'true');
        
        // Hide the other content
        otherContent.classList.remove('active');
        otherContent.style.maxHeight = '0';
        otherContent.style.opacity = '0';
        
        // Add hidden class after transition completes
        setTimeout(() => {
          if (!otherContent.classList.contains('active')) {
            otherContent.classList.add('hidden');
          }
        }, 300);
      }
    });
  }
  
  // Add keyboard accessibility
  faqToggles.forEach(toggle => {
    toggle.addEventListener('keydown', function(e) {
      // If Enter or Space key is pressed
      if (e.key === 'Enter' || e.key === ' ') {
        e.preventDefault();
        toggleFaq(this);
      }
      
      // Arrow key navigation
      if (e.key === 'ArrowDown') {
        e.preventDefault();
        const nextToggle = this.closest('.bg-white').nextElementSibling?.querySelector('.faq-toggle');
        if (nextToggle) nextToggle.focus();
      }
      
      if (e.key === 'ArrowUp') {
        e.preventDefault();
        const prevToggle = this.closest('.bg-white').previousElementSibling?.querySelector('.faq-toggle');
        if (prevToggle) prevToggle.focus();
      }
    });
  });
  
  // Handle deep linking if URL contains hash to a specific FAQ
  if (window.location.hash) {
    const targetId = window.location.hash.substring(1);
    const targetContent = document.getElementById(targetId);
    
    if (targetContent && targetContent.classList.contains('faq-content')) {
      const targetToggle = targetContent.previousElementSibling;
      
      // Scroll to the target and open it
      setTimeout(() => {
        targetToggle.scrollIntoView({ behavior: 'smooth', block: 'center' });
        
        // Open the FAQ item after scrolling
        setTimeout(() => {
          if (!targetToggle.classList.contains('active')) {
            toggleFaq(targetToggle);
          }
        }, 500);
      }, 100);
    }
  }
  
  // Optional: Add search functionality for FAQs
  const searchInput = document.getElementById('faqSearch');
  if (searchInput) {
    searchInput.addEventListener('input', function() {
      const searchTerm = this.value.toLowerCase().trim();
      
      if (searchTerm.length > 1) {
        faqToggles.forEach(toggle => {
          const faqText = toggle.textContent.toLowerCase();
          const faqContent = toggle.nextElementSibling.textContent.toLowerCase();
          const faqItem = toggle.closest('.bg-white');
          
          if (faqText.includes(searchTerm) || faqContent.includes(searchTerm)) {
            faqItem.style.display = '';
            
            // Highlight matching text (optional)
            const span = toggle.querySelector('span');
            const originalText = span.getAttribute('data-original') || span.textContent;
            
            if (!span.getAttribute('data-original')) {
              span.setAttribute('data-original', originalText);
            }
            
            // Highlight the matching part
            const regex = new RegExp(`(${searchTerm})`, 'gi');
            span.innerHTML = originalText.replace(regex, '<mark>$1</mark>');
          } else {
            faqItem.style.display = 'none';
          }
        });
      } else {
        // Reset display and text
        faqToggles.forEach(toggle => {
          toggle.closest('.bg-white').style.display = '';
          
          const span = toggle.querySelector('span');
          const originalText = span.getAttribute('data-original');
          if (originalText) {
            span.innerHTML = originalText;
          }
        });
      }
    });
  }
});


