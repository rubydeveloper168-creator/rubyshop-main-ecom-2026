  function startCountdown(endTime) {
  // If no end time is provided, set default to 3 days from now
  if (!endTime) {
    endTime = new Date();
    endTime.setDate(endTime.getDate() + 4); // Set to 3 days from now
  }

  // Update the countdown every second
  const countdownInterval = setInterval(function() {
    // Get current time
    const now = new Date().getTime();
    
    // Calculate the time remaining
    const timeRemaining = endTime - now;
    
    // If countdown is finished
    if (timeRemaining <= 0) {
      clearInterval(countdownInterval);
      document.getElementById('days-summary').textContent = '00';
      document.getElementById('hours-summary').textContent = '00';
      document.getElementById('minutes-summary').textContent = '00';
      document.getElementById('seconds-summary').textContent = '00';
      return;
    }
    
    // Calculate days, hours, minutes, and seconds
    const days = Math.floor(timeRemaining / (1000 * 60 * 60 * 24));
    const hours = Math.floor((timeRemaining % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((timeRemaining % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((timeRemaining % (1000 * 60)) / 1000);
    
    // Add leading zeros if needed
    const formattedDays = days < 10 ? '0' + days : days;
    const formattedHours = hours < 10 ? '0' + hours : hours;
    const formattedMinutes = minutes < 10 ? '0' + minutes : minutes;
    const formattedSeconds = seconds < 10 ? '0' + seconds : seconds;
    
    // Update the HTML elements
    document.getElementById('days-summary').textContent = formattedDays;
    document.getElementById('hours-summary').textContent = formattedHours;
    document.getElementById('minutes-summary').textContent = formattedMinutes;
    document.getElementById('seconds-summary').textContent = formattedSeconds;
  }, 1000);
  
  return countdownInterval; // Return interval ID so it can be cleared if needed
}

// Call the function when the page loads
document.addEventListener('DOMContentLoaded', function() {
  startCountdown();
});


</script>





    
