<script>
  document.addEventListener('DOMContentLoaded', function() {
  // Slider elements
  const slidesTrack = document.getElementById('slidesTrack');
  const prevBtn = document.getElementById('prevBtn');
  const nextBtn = document.getElementById('nextBtn');
  const paginationDots = document.querySelectorAll('.pagination-dot');
  
  // Slider configuration
  let slidesToShow = window.innerWidth >= 768 ? 3 : 1; // Show 3 on desktop, 1 on mobile
  let slidesToScroll = window.innerWidth >= 768 ? 3 : 1; // Scroll 3 on desktop, 1 on mobile
  let currentIndex = 0;
  const slides = document.querySelectorAll('#slidesTrack > div');
  const totalSlides = slides.length;
  
  // Calculate max index based on current configuration
  function getMaxIndex() {
    return totalSlides - slidesToShow;
  }
  
  // Update slider position
  function updateSliderPosition() {
    const slideWidth = 100 / slidesToShow;
    slidesTrack.style.transform = `translateX(-${currentIndex * slideWidth}%)`;
  }
  
  // Update pagination dots
  function updatePaginationDots() {
    const totalPages = Math.ceil((totalSlides - slidesToShow + 1) / slidesToScroll);
    const activePage = Math.floor(currentIndex / slidesToScroll);
    
    // Make sure we have the right number of dots
    const dotsContainer = document.getElementById('paginationDots');
    
    // Clear existing dots if needed
    if (dotsContainer.children.length !== totalPages) {
      dotsContainer.innerHTML = '';
      
      // Create new dots
      for (let i = 0; i < totalPages; i++) {
        const dot = document.createElement('button');
        dot.className = `w-4 h-4 rounded-full mx-2 focus:outline-none pagination-dot ${i === activePage ? 'bg-red-500' : 'bg-gray-300'}`;
        dot.setAttribute('data-index', i);
        
        // Add click event
        dot.addEventListener('click', function() {
          currentIndex = i * slidesToScroll;
          if (currentIndex > getMaxIndex()) currentIndex = getMaxIndex();
          updateSliderPosition();
          updatePaginationDots();
        });
        
        dotsContainer.appendChild(dot);
      }
    } else {
      // Just update active state
      paginationDots.forEach((dot, index) => {
        if (index === activePage) {
          dot.classList.add('bg-red-500');
          dot.classList.remove('bg-gray-300');
        } else {
          dot.classList.remove('bg-red-500');
          dot.classList.add('bg-gray-300');
        }
      });
    }
  }
  
  // Previous button click handler
  prevBtn.addEventListener('click', function() {
    if (currentIndex > 0) {
      currentIndex -= slidesToScroll;
      if (currentIndex < 0) currentIndex = 0;
      updateSliderPosition();
      updatePaginationDots();
    }
  });
  
  // Next button click handler
  nextBtn.addEventListener('click', function() {
    const maxIndex = getMaxIndex();
    if (currentIndex < maxIndex) {
      currentIndex += slidesToScroll;
      if (currentIndex > maxIndex) currentIndex = maxIndex;
      updateSliderPosition();
      updatePaginationDots();
    }
  });
  
  // Handle responsive behavior
  function handleResponsive() {
    const wasDesktop = slidesToShow === 3;
    const isDesktop = window.innerWidth >= 768;
    
    // Only update if there's a change in view mode
    if (wasDesktop !== isDesktop) {
      slidesToShow = isDesktop ? 3 : 1;
      slidesToScroll = isDesktop ? 3 : 1;
      
      // Reset position to avoid issues
      currentIndex = 0;
      
      // Update slider
      updateSliderPosition();
      updatePaginationDots();
    }
  }
  
  // Listen for window resize
  window.addEventListener('resize', handleResponsive);
  
  // Initialize slider
  updateSliderPosition();
  updatePaginationDots();
  
  // Initial responsive check
  handleResponsive();
  
  // Add keyboard navigation
  document.addEventListener('keydown', function(e) {
    if (e.key === 'ArrowLeft') {
      prevBtn.click();
    } else if (e.key === 'ArrowRight') {
      nextBtn.click();
    }
  });
  
  // Add swipe support for mobile
  let touchStartX = 0;
  let touchEndX = 0;
  
  slidesTrack.addEventListener('touchstart', function(e) {
    touchStartX = e.changedTouches[0].screenX;
  }, { passive: true });
  
  slidesTrack.addEventListener('touchend', function(e) {
    touchEndX = e.changedTouches[0].screenX;
    handleSwipe();
  }, { passive: true });
  
  function handleSwipe() {
    const swipeThreshold = 50;
    
    if (touchEndX < touchStartX - swipeThreshold) {
      // Swipe left - next slide
      nextBtn.click();
    }
    
    if (touchEndX > touchStartX + swipeThreshold) {
      // Swipe right - previous slide
      prevBtn.click();
    }
  }
});



