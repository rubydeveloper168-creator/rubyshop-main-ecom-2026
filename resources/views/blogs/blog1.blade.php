    // Google Form mapping
    const googleFormURL = 'https://docs.google.com/forms/d/e/1FAIpQLScQ3LjJBELRFViTGhNwJ4YnekoT063jlEwa0gvIlVUfe_lQLQ/formResponse';
    const fieldMapping = {
        'name': 'entry.1303366296',
